class LongExample1 {
    public static void main(String[] args) {
	    System.out.println("Long variables declared:");
        long worldPopulation = 7800000000L;
		System.out.println("World Population: " + worldPopulation);
        long nationalDebt = 2147483648000L;
		System.out.println("National Debt: " + nationalDebt);
        long distanceToAlphaCentauriInKm = 40000000000000L;
		System.out.println("Distance to Alpha Centauri (in km): " + distanceToAlphaCentauriInKm);
        long ageOfEarthInYears = 4500000000L;
		System.out.println("Age of Earth (in years): " + ageOfEarthInYears);
        long bytesInTerabyte = 1099511627776L;
        System.out.println("Bytes in a Terabyte: " + bytesInTerabyte);
		long maxLongValue = 9223372036854775807L;
		System.out.println("Max Long Value: " + maxLongValue);
        long minLongValue = -9223372036854775808L;
		System.out.println("Min Long Value: " + minLongValue);
        long grainsOfSandOnEarth = 7000000000000000000L;
		System.out.println("Grains of Sand on Earth: " + grainsOfSandOnEarth);
        long waterMoleculesInLiter = 330000000000000000L;
		System.out.println("Water Molecules in a Liter: " + waterMoleculesInLiter);
        long starsInMilkyWay = 100000000000L;
		System.out.println("Stars in Milky Way: " + starsInMilkyWay);
        
 }
}