public class FloatExample {
    public static void main(String[] args) {
        System.out.println("Float variables declared:");
		float pi = 3.14159f;
        System.out.println("Pi: " + pi);
		float gravity = 9.81f;
        System.out.println("Gravity: " + gravity);
		float averageTemperature = 22.5f;
        System.out.println("Average Temperature: " + averageTemperature);
		float distanceToSunInMillionKm = 149.6f;
        System.out.println("Distance to Sun (in million km): " + distanceToSunInMillionKm);
		float lightSpeedInMillionMps = 299.792f;
		System.out.println("Light Speed (in million meters per second): " + lightSpeedInMillionMps);
        float fuelEfficiency = 15.5f;
		System.out.println("Fuel Efficiency: " + fuelEfficiency);
        float heightInMeters = 1.75f;
		System.out.println("Height (in meters): " + heightInMeters);
        float weightInKg = 70.5f;
		System.out.println("Weight (in kg): " + weightInKg);
        float interestRate = 3.75f;
		System.out.println("Interest Rate: " + interestRate);
        float exchangeRate = 1.13f;
		System.out.println("Exchange Rate: " + exchangeRate);
       
    }
}