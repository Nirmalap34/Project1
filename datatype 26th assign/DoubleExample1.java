class DoubleExample1 {
    public static void main(String[] args) {
        System.out.println("Double variables declared:");
		double earthCircumferenceInKm = 40075.017;
		System.out.println("Earth Circumference (in km): " + earthCircumferenceInKm);
        double speedOfLightInMps = 299792458.0;
		System.out.println("Speed of Light (in m/s): " + speedOfLightInMps);
        double planckConstant = 6.62607015e-34;
		System.out.println("Planck Constant: " + planckConstant);
        double avogadroNumber = 6.02214076e23;
		System.out.println("Avogadro Number: " + avogadroNumber);
        double gravitationalConstant = 6.67430e-11;
		System.out.println("Gravitational Constant: " + gravitationalConstant);       
        double eulerNumber = 2.718281828459;
		System.out.println("Euler's Number: " + eulerNumber);
        double goldenRatio = 1.61803398875;
		System.out.println("Golden Ratio: " + goldenRatio);
        double electronMassInKg = 9.10938356e-31;
		System.out.println("Electron Mass (in kg): " + electronMassInKg);
        double protonMassInKg = 1.67262192369e-27;
		System.out.println("Proton Mass (in kg): " + protonMassInKg);
        double standardAtmosphericPressure = 101325.0;
		System.out.println("Standard Atmospheric Pressure: " + standardAtmosphericPressure);
        
    }
}