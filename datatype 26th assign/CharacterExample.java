public class CharacterExample {
    public static void main(String[] args) {
	    System.out.println("Character variables declared:");
        char initial = 'A';
		System.out.println("Initial: " + initial);
        char grade = 'B';
		System.out.println("Grade: " + grade);
        char currencySymbol = '$';
		System.out.println("Currency Symbol: " + currencySymbol);
        char punctuationMark = '!';
		System.out.println("Punctuation Mark: " + punctuationMark);
        char vowel = 'E';
		System.out.println("Vowel: " + vowel);
        char consonant = 'T';
		System.out.println("Consonant: " + consonant);
        char digit = '5';
		System.out.println("Digit: " + digit);
        char mathOperator = '+';
		System.out.println("Math Operator: " + mathOperator);
        char escapeCharacter = '\n'; // Newline character
		System.out.println("Escape Character: " + escapeCharacter);
        char hexChar = '\u0041'; // Unicode representation for 'A'
		System.out.println("Hex Character: " + hexChar);

     }
}